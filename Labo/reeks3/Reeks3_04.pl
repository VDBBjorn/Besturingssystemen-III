Deze klasse vind je onder "WMI Registry Classes" en noemt "StdRegProv". 
Vanaf Windows NT 6.0 vind je deze klasse terug in twee namespaces "root\default" en "root\cimv2".

Deze klasse heeft geen instanties en ook geen eigen attributen.
De klasse heeft wel veel statische methodes, die de functionaliteit van het register ter beschikking stellen op een "object-georienteerde" manier.

