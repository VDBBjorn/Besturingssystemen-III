(a) Zoek een klasse met attribuut "freespace": je vindt de klasse "CIM_LogicalDisk", met als onderliggende Win32-klasse "Win32_LogicalDisk". Het attribuut "FreeSpace" bevat de vrije ruimte van elke partitie
(b)(c) Zoek de klasse die iets te maken heeft met "NetConnection" of "Network" 
           -> de klasse "Win32_NetworkAdapter"
       Deze klasse heeft de attributen "NetConnectionID" (b), "MACAddress" (c) 
(d) Zoek een klasse met attribuut "IpAddress": 
           -> "IPAddress" is attribuut van "Win32_NetworkAdapterConfiguration"
(e) Zoek een klasse met attribuut "servicepack": 
           -> "ServicePackMajorVersion" is attribuut van "Win32_OperatingSystem"


